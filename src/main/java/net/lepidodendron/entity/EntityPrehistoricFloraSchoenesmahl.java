	public boolean findGrappleTarget() {
		//System.err.println("finding grapple target");
		if (this.willGrapple || this.getIsCuriousWalking()) {
			return false;
		}
		List<EntityPrehistoricFloraSchoenesmahl> Schoenesmahl = world.getEntitiesWithinAABB(EntityPrehistoricFloraSchoenesmahl.class, new AxisAlignedBB(this.getPosition().add(-8, -4, -8), this.getPosition().add(8, 4, 8)));
		for (EntityPrehistoricFloraSchoenesmahl currentSchoenesmahl : Schoenesmahl) {
			if ((!currentSchoenesmahl.getIsCuriousWalking()) && currentSchoenesmahl.isPFAdult() && this.isPFAdult() && currentSchoenesmahl != this && (!currentSchoenesmahl.willGrapple) && this.canEntityBeSeen(currentSchoenesmahl)) {
				this.setGrappleTarget(currentSchoenesmahl);
				currentSchoenesmahl.willGrapple = true;
				this.willGrapple = true;
				currentSchoenesmahl.setGrappleTarget(this);
				return true;
			}
		}
		return false;
	}

