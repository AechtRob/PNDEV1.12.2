	public boolean findGrappleTarget() {
		//System.err.println("finding grapple target");
		if (this.willGrapple || this.getIsCuriousWalking()) {
			return false;
		}
		List<EntityPrehistoricFloraTawa> Tawa = world.getEntitiesWithinAABB(EntityPrehistoricFloraTawa.class, new AxisAlignedBB(this.getPosition().add(-8, -4, -8), this.getPosition().add(8, 4, 8)));
		for (EntityPrehistoricFloraTawa currentTawa : Tawa) {
			if ((!currentTawa.getIsCuriousWalking()) && currentTawa.isPFAdult() && this.isPFAdult() && currentTawa != this && (!currentTawa.willGrapple) && this.canEntityBeSeen(currentTawa)) {
				this.setGrappleTarget(currentTawa);
				currentTawa.willGrapple = true;
				this.willGrapple = true;
				currentTawa.setGrappleTarget(this);
				return true;
			}
		}
		return false;
	}

